    <h1>VR医学体験</h1>
    <hr /><br />
    <h2>VRとは？</h2>
    <p>みなさんはVRというものを体験したことはありますか?VRとはVirtualReality(仮想現実)の略で、コンピュータが作った仮想空間を、まるで自分が本当にそこにいるかのように体験できる技術のことを指します。近年急速に発達してきた技術で、ビジネス・医療・エンターテインメントなどの幅広い分野で活用されています。みなさんも街中のVR体験コーナーに行列が出来ているのを見たことがあるのではないでしょうか。</p>
    <h2>医療でのVR</h2>
    <p>今回の企画では，医療に関係する以下のプログラムを実際に体験することができます。</p>
    <h3>・Heart explorer(富士通)</h3>
    <p>高度なシミュレーション技術によって作成された心臓の3Dモデルを用いて、正常/疾患状態の心臓を様々な角度から見ることができます。なかなか実際に触れる機会の無い心臓ですが、VRの技術を活用することによって、医療従事者が早く正確に知識を習得することができます。</p>
    <h3>・認知症・統合失調症体験VR(シルバーウッド・ヤンセンファーマ)</h3>
    <p>みなさんこれらの病気の名前を耳にしたことはあると思いますが、具体的な症状をご存知でしょうか?おそらく，多くの人が記憶障害、あるいは妄想や幻覚などを頭に思い浮かべるでしょう。しかし実際にはもっと様々な症状があります。自分の身近な人がこれらの病気になってしまった際、早期発見(と治療)のために，これらの様々な症状を正しく理解することは非常に大切なことです。これらのVRはそのようなコンセプトのもとで，医療従事者以外の方にも具体的な症状を理解してもらえるように作られました。</p>
    <p><br /><br />いずれのVRも非常にリアリティがあり、とても貴重な経験となることは間違いありません。皆様のご来場を心よりお待ちしております。</p>
