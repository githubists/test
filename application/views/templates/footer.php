  <p>医療ドラマといえば、いつの時代もドラマの花形。江戸時代にタイムスリップした医師の運命の物語「仁」や、"失敗しない"フリーランス女性医師の活躍を描く「ドクターX」などなど。しかし一方で、ドラマや映画の医療シーンには「今、何が起こっているんだろう?」「こんなことって本当にあるのかな」と様々な「?」があふれることもしばしば。「白い巨塔」の総回診のシーンに目を疑った方も少なくないはずです! そこで、東大医学部生が医学知識を総動員し、必要とあらば現役の医師をも訪ねて、あなたの「?」に答えました!</p>
  <h3>企画内容について</h3>
  <p>実際にテレビで放映されたシーンを画面に流しつつ、これらのシーンの解説を試みます。身近なお薬の話から現場で実装されている最先端医療の話まで、幅広いテーマについてご紹介できるように、解説するシーンについては厳選しました。また、普段はドラマを見ないという方にも楽しんでいただけるように、そのドラマのあらすじにも多少踏み込みます。この機会に、医学部生が観る医療ドラマの世界の一端をのぞいてみませんか?</p>
