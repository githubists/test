  <h2>血圧ってなんだろう?</h2>
  <p>みなさんは血圧と聞いて何を思い浮かべますか?「上の血圧と下の血圧の2つがある」「高血圧だと病気になるリスクが高い」「測る機械が腕を締め付けて痛い」といったところでしょうか。では「上と下の血圧ってなんのことだろう?」「どうして高血圧だと病気になりやすいんだろう?」「測定する機械はなぜ腕を締め付けるんだろう?」と考えたことはありますか?</p>
  <p>この企画では、そのような血圧に関する疑問に私たち医学部生が答えるとともに、みなさんご自身で血圧を測っていただきます。この冊子ではまず、血圧の上と下とは何かについてのみ簡単にご説明します!</p>
  <h2>血圧の「上」と「下」とは</h2>
  <p>血圧を測定すると、120/80(上が120、下が80)などと上下2つの値が返ってきます。この上の値(ここでは120)は心臓が収縮して血液を送り出す時の血圧の値です。下の値(80)は心臓が弛緩して血液が流れ込む時の値です。すなわち、心臓がポンプのように収縮・弛緩を繰り返して血液を送り出しており、血管にかかる圧力が絶えず変動しているので、上下2つの値で血圧を表しているというわけですね。</p>
  <h2>みなさんにも「医師」になってもらいます!</h2>
  <p>本企画では、みなさんに血圧を測る側の立場になっていただきます。「血圧って機械で測るんじゃないの?」と思った方、実は聴診器といくつかの道具を使うだけで、電子機器を用いずに、上と下の血圧を両方測ることができるんです!この仕組みはとても良く出来ていて、筆者自身知ったときに感動しました。少しでも気になった方はぜひ本企画にいらしてください。</p>
  <p>血圧を測る仕組みを理解し、血圧を測定することができるようになったあなたは、きっと医師になった気分になれるはずです!</p>
