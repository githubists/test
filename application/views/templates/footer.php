    <h1>VR医学体験</h1>
    <hr /><br />
    <h2>VRとは?</h2>
    <p>皆さん、VRというものを体験したことはあるでしょうか? VRはVirtual Reality(仮想現実)の略で，コンピュータが作った仮想空間を、まるで自分が本当にそこにいるかのように体験できる技術のことを指しています。近年急速に発達してきた分野で、ビジネス・医療・エンターテインメントなどの幅広い分野で活用されています。皆さんも街中のVR体験コーナーに行列が出来ているのを見たことがあるのではないでしょうか。</p>
    <h2>医療でのVR</h2>
    <h3>Heart explorer(富士通)</h3>
    <p>高度なシミュレーション技術によって作成された心臓の3Dモデルを用いて、心臓の構造や疾患状態を様々な角度から見ることができます。なかなか実際に触れることの出来ない心臓ですが、VRの技術を活用することによって、医療従事者が早く正確に知識を習得することができます。</p>
    <h3>認知症・統合失調症体験VR(シルバーウッド・ヤンセンファーマ)</h3>
    <p>皆さんこれらの病気の名前を耳にしたことはあると思いますが，具体的な症状をご存知でしょうか? 恐らく、ほとんどの人が記憶障害、あるいは妄想や幻覚以外の症状を知らないと思います。ですが、自分の友人や家族がこれらの病気になってしまった際に、早期発見や病気の進行に大きく関わるという点で、様々な症状を正しく理解することは非常に大切なことです。これらのVRはそのようなコンセプトのもとで、医療従事者以外の方にも具体的な症状を理解してもらえるように作られました。</p>
    <p>いずれのVRも非常にリアリティがあり、VRを体験してみたい方や、医療の世界をのぞいてみたいという方にとって、とても貴重な経験となることは間違いありません。それでは皆様のご来場を心よりお待ちしております。</p>
