  <h1>血圧測定</h1>
  <hr /><br />
  <h2>血圧を"自分で"測ってみよう!</h2>
  <h3>血圧ってなんだろう?</h3>
  <p>みなさんは血圧と聞いて何を思い浮かべますか?「上の血圧と下の血圧の2つがある」「高血圧だと病気のリスクが高い」「測る機械が腕を締め付けて痛い」といったところでしょうか。では「上と下の血圧ってなんのことだろう?」「どうして高血圧だと病気になりやすいんだろう?」「測定する機械はなぜ腕を締め付けるんだろう?」と考えたことはありますか?</p>
  <p>この企画では、そのような血圧に関する疑問に私たち医学部生が答えるとともに、みなさんご自身で血圧を測っていただきます。この冊子ではまず、血圧の上と下とは何かについてのみ簡単に説明しますね。</p>
  <h3>血圧の「上」と「下」とは</h3>
  <p>血圧を測定すると、120/80(上が120、下が80)などと上下2つの値が返ってきます。この上の値(ここでは120)は心臓が収縮して血液を送り出す時の血圧の値です。下の値(80)は心臓が拡張して血液を吸い込む時の値です。すなわち、心臓がポンプのように、血液を送り出し吸い込む事を繰り返していて、血管にかかる圧力が絶えず変動しているので、上下2つの値で血圧を表しているというわけですね。</p>
  <h3>みなさんにも医師になってもらいます!</h3>
  <p>本企画ではさらに、みなさんに血圧を測る側の立場にもなっていただきます。「血圧って機械で測るんじゃないの?」と思った方、実は聴診器といくつかの道具を使うだけで、電子機器を用いずに、上と下の血圧を両方測ることができるんです! この仕組みはとても良く出来ていて、筆者自身知ったときに感動しました。ですから、気になった方はぜひ本企画にいらしてください。</p>
  <p>血圧を測る仕組みを理解して、血圧を測定することができるようになったあなたは、医師になった気分になること間違いなし!</p>
