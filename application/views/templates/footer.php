  <h2>展覧会</h2>
  <p>美術と聞くと、皆さんは何を思い浮かべるでしょうか。絵画やデザインなどの作品制作をイメージする方も多いでしょう。もしくは、美術館巡りを真っ先に思い浮かべる方もいるかもしれません。作品制作に関わることは芸術の本質とも言えますが、歴史の風雪に耐えた数々の名画を美術館という非日常な世界で眺めるというのもまた、なんとも乙なものです。</p>
  <p>さて、私たち美術部 ( 踏朱会 ) の活動としては大きく分 けて、制作と美術館巡りの2つがあります。  まず制作に関しては、油彩・水彩・鉛筆画などの作品 制作を中心に、展覧会へ向けて作品を制作しています。 昨年度は、五月祭、OB 組織である煉瓦の会の展覧会へ の出展、東大病院での煉瓦の会との合同展を行いました。</p>
  <p>美術館巡りに関しては、部員が行きたいところに 皆で行くといったかたちで、1・2 ヶ月に 1 回ほどの頻 度で行っています。大きな展覧会をメインに、規模が小さくても面白そうなものには行っており、4月始めには新歓として毎年新入生と行っています。直近の大きな展覧会としてはクリムト展などを考えています。</p>
